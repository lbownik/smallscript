package ss.parser;

import ss.runtime.SSLong;
import ss.runtime.SSObject;

/*******************************************************************************
 * @author lukasz.bownik@gmail.com
 ******************************************************************************/
public final class LongConstant implements Expression {

	/****************************************************************************
	 * 
	****************************************************************************/
	public LongConstant(final Long value) {
		
		this.value = value;
	}
	/****************************************************************************
	 * 
	****************************************************************************/
	@Override
	public Long value() {
		
		return this.value;
	}
	/****************************************************************************
	 * 
	****************************************************************************/
	@Override
	public String toString() {
		
		return "LongConstant: " + this.value;
	}

	/****************************************************************************
	 * 
	****************************************************************************/
	@Override
	public int hashCode() {

		return this.value.hashCode();
	}

	/****************************************************************************
	 * 
	****************************************************************************/
	@Override
	public boolean equals(final Object o) {

		if (o != null && getClass() == o.getClass()) {
			return this.value.equals(((LongConstant) o).value);
		} else {
			return false;
		}
	}
	/****************************************************************************
	 * 
	****************************************************************************/
	public SSObject toSSObject() {

		return new SSLong(this.value);
	}


	/****************************************************************************
	 * 
	****************************************************************************/
	private final Long value;
}
